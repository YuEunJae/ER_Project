
package com.erproject.command;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@NoArgsConstructor
@AllArgsConstructor
public class NoticeImageVO {

	public NoticeImageVO() {
		
	}
	private int ni_No; // 이미지번호
	private int notice_No; // 글 번호
	private String ni_Path; // 이미지 경로
	private String ni_Name; // 이미지 이름
	public NoticeImageVO(int ni_No, int notice_No, String ni_Path, String ni_Name) {
		super();
		this.ni_No = ni_No;
		this.notice_No = notice_No;
		this.ni_Path = ni_Path;
		this.ni_Name = ni_Name;
	}
	public int getNi_No() {
		return ni_No;
	}
	public void setNi_No(int ni_No) {
		this.ni_No = ni_No;
	}
	public int getNotice_No() {
		return notice_No;
	}
	public void setNotice_No(int notice_No) {
		this.notice_No = notice_No;
	}
	public String getNi_Path() {
		return ni_Path;
	}
	public void setNi_Path(String ni_Path) {
		this.ni_Path = ni_Path;
	}
	public String getNi_Name() {
		return ni_Name;
	}
	public void setNi_Name(String ni_Name) {
		this.ni_Name = ni_Name;
	}
	@Override
	public String toString() {
		return "NoticeImageVO [ni_No=" + ni_No + ", notice_No=" + notice_No + ", ni_Path=" + ni_Path + ", ni_Name="
				+ ni_Name + "]";
	}
	
	
	
	public NoticeImageVO() {
		
	}
	public NoticeImageVO(int ni_No, int notice_No, String ni_Path, String ni_Name) {
		super();
		this.ni_No = ni_No;
		this.notice_No = notice_No;
		this.ni_Path = ni_Path;
		this.ni_Name = ni_Name;
	}
	
	public int getNi_No() {
		return ni_No;
	}
	public void setNi_No(int ni_No) {
		this.ni_No = ni_No;
	}
	public int getNotice_No() {
		return notice_No;
	}
	public void setNotice_No(int notice_No) {
		this.notice_No = notice_No;
	}
	public String getNi_Path() {
		return ni_Path;
	}
	public void setNi_Path(String ni_Path) {
		this.ni_Path = ni_Path;
	}
	public String getNi_Name() {
		return ni_Name;
	}
	public void setNi_Name(String ni_Name) {
		this.ni_Name = ni_Name;
	}
	@Override
	public String toString() {
		return "NoticeImageVO [ni_No=" + ni_No + ", notice_No=" + notice_No + ", ni_Path=" + ni_Path + ", ni_Name="
				+ ni_Name + "]";
	}
	
	
	
}
